export const transformTitle = () => {
	switch (title) {
		case "NightElf":
			return "Night Elf";
			break;
		case "VoidElf":
			return "Void Elf";
			break;
		case "LightforgedDraenei":
			return "Lightforged Draenei";
			break;
		case "DarkIronDwarf":
			return "Dark Iron Dwarf";
			break;
		case "KulTiran":
			return "Kul Tiran";
			break;
		case "DeathKnight":
			return "Death Knight";
			break;
		case "DemonHunter":
			return "Demon Hunter";
			break;
	}
};
